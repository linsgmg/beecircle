package com.xz.beecircle.bean;

public class BackDataBean2 {
    private String id;
    private String pics;

    public BackDataBean2() {
    }

    public BackDataBean2(String id, String pics) {
        this.id = id;
        this.pics = pics;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getPics() {
        return pics;
    }

    public void setPics(String pics) {
        this.pics = pics;
    }
}
